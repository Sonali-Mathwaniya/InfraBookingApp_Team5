package com.zensar.training.ci;

public class Size {
   private int size;

 
   
public Size() {
	super();
}

 
public Size(int size) {
	super();
	this.size = size;
}


public int getSize() {
	return size;
}


public void setSize(int size) {
	this.size = size;
}


@Override
public String toString() {
	return "Size [size=" + size + "]";
}
   
   
   
   
   
	
	
	
}
